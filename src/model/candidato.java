package model;

public class candidato {

	private int idCandidato;
	private String nomCandidato, apeCandidato, anioRegCandidato, partido;
	
	
	public int getIdCandidato() {	return idCandidato;}
	public void setIdCandidato(int idCandidato) {	this.idCandidato = idCandidato;}
	
	
	
	public String getAnioRegCandidato() {	return anioRegCandidato;}
	public void setAnioRegCandidato(String anioRegCandidato) {	this.anioRegCandidato = anioRegCandidato;}
	
	
	
	public String getNomCandidato() {	return nomCandidato;}
	public void setNomCandidato(String nomCandidato) {	this.nomCandidato = nomCandidato;}
	
	
	
	public String getApeCandidato() {	return apeCandidato;}
	public void setApeCandidato(String apeCandidato) {	this.apeCandidato = apeCandidato;}
	
	
	
	public String getPartido() {	return partido;}
	public void setPartido(String partido) {	this.partido = partido;}
	
	

}
